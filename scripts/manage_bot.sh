                ;;
            8)
                # TODO: Implement cleanup old logs and backups
                log "YELLOW" "‚ö†Ô∏è –§—É–Ω–∫—Ü–∏—è –æ—á–∏—Å—Ç–∫–∏ —Å—Ç–∞—Ä—ã—Ö –ª–æ–≥–æ–≤ –∏ –±—ç–∫–∞–ø–æ–≤ –µ—â–µ –Ω–µ —Ä–µ–∞–ª–∏–∑–æ–≤–∞–Ω–∞."
                ;;
            0)
                log "BLUE" "üö™ –í—ã—Ö–æ–¥..."
