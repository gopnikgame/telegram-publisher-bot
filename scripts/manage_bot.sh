                    log "RED" "‚ùå –§–∞–π–ª –ª–æ–≥–æ–≤ –Ω–µ –Ω–∞–π–¥–µ–Ω: $BOT_LOG_FILE"
                fi
                ;;
            6)
                # –ü–æ–∫–∞–∑–∞—Ç—å –ª–æ–≥–∏ –æ—à–∏–±–æ–∫
                log "RED" "‚ùå –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ª–æ–≥–∏ –æ—à–∏–±–æ–∫ –±–æ—Ç–∞..."
                if [ -f "$ERROR_LOG_FILE" ]; then
                    cat "$ERROR_LOG_FILE"
                else
                    log "RED" "‚ùå –§–∞–π–ª –ª–æ–≥–æ–≤ –æ—à–∏–±–æ–∫ –Ω–µ –Ω–∞–π–¥–µ–Ω: $ERROR_LOG_FILE"
